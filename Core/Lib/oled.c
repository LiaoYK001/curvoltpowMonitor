u8 OLED_GRAM[144][8];

// 锟斤拷锟皆猴拷锟斤拷
void OLED_ColorTurn(u8 i) {
  if (i == 0) {
    OLED_WR_Byte(0xA6, OLED_CMD); // 锟斤拷锟斤拷锟斤拷示
  }
  if (i == 1) {
    OLED_WR_Byte(0xA7, OLED_CMD); // 锟斤拷色锟斤拷示
  }
}

// 锟斤拷幕锟斤拷转180锟斤拷
void OLED_DisplayTurn(u8 i) {
  if (i == 0) {
    OLED_WR_Byte(0xC8, OLED_CMD); // 锟斤拷锟斤拷锟斤拷示
    OLED_WR_Byte(0xA1, OLED_CMD);
  }
  if (i == 1) {
    OLED_WR_Byte(0xC0, OLED_CMD); // 锟斤拷转锟斤拷示
    OLED_WR_Byte(0xA0, OLED_CMD);
  }
}

// 锟斤拷时
void IIC_delay(void) {
  u8 t = 3;
  while (t--)
    ;
}

// 锟斤拷始锟脚猴拷
void I2C_Start(void) {
  OLED_SDA_Set();
  OLED_SCL_Set();
  IIC_delay();
  OLED_SDA_Clr();
  IIC_delay();
  OLED_SCL_Clr();
  IIC_delay();
}

// 锟斤拷锟斤拷锟脚猴拷
void I2C_Stop(void) {
  OLED_SDA_Clr();
  OLED_SCL_Set();
  IIC_delay();
  OLED_SDA_Set();
}

// 锟饺达拷锟脚猴拷锟斤拷应
void I2C_WaitAck(void) // 锟斤拷锟斤拷锟斤拷锟脚号的碉拷平
{
  OLED_SDA_Set();
  IIC_delay();
  OLED_SCL_Set();
  IIC_delay();
  OLED_SCL_Clr();
  IIC_delay();
}

// 写锟斤拷一锟斤拷锟街斤拷
void Send_Byte(u8 dat) {
  u8 i;
  for (i = 0; i < 8; i++) {
    if (dat & 0x80) // 锟斤拷dat锟斤拷8位锟斤拷锟斤拷锟轿伙拷锟斤拷锟叫达拷锟�
    {
      OLED_SDA_Set();
    } else {
      OLED_SDA_Clr();
    }
    IIC_delay();
    OLED_SCL_Set();
    IIC_delay();
    OLED_SCL_Clr(); // 锟斤拷时锟斤拷锟脚猴拷锟斤拷锟斤拷为锟酵碉拷平
    dat <<= 1;
  }
}

// 锟斤拷锟斤拷一锟斤拷锟街斤拷
// mode:锟斤拷锟斤拷/锟斤拷锟斤拷锟街�
// 0,锟斤拷示锟斤拷锟斤拷;1,锟斤拷示锟斤拷锟斤拷;
void OLED_WR_Byte(u8 dat, u8 mode) {
  I2C_Start();
  Send_Byte(0x78);
  I2C_WaitAck();
  if (mode) {
    Send_Byte(0x40);
  } else {
    Send_Byte(0x00);
  }
  I2C_WaitAck();
  Send_Byte(dat);
  I2C_WaitAck();
  I2C_Stop();
}

